package optAlgos;

public class Pair<L,R> {
    private L x;
    private R y;
    public Pair(L l, R r){
        this.x = l;
        this.y = r;
    }
    public L getL(){ return x; }
    public R getR(){ return y; }
    public void setL(L l){ this.x = l; }
    public void setR(R r){ this.y = r; }
    
    
}